#!/usr/bin/env node

import { Server } from "@modelcontextprotocol/sdk/server/index.js";
import { StdioServerTransport } from "@modelcontextprotocol/sdk/server/stdio.js";
import {
  CallToolRequestSchema,
  ListToolsRequestSchema,
} from "@modelcontextprotocol/sdk/types.js";
import { WebClient } from '@slack/web-api';
import * as crypto from 'crypto';
import * as fs from 'fs';
import * as path from 'path';
import { Logger } from './logger.js';

// Shared file-based IPC channel between the main bot process (button clicks
// arrive there) and the permission-prompt MCP subprocess (which awaits the
// decision). systemd's StateDirectory= creates /var/lib/claude-slack-bridge
// at mode 0700 owned by the service user; we put approvals under that so
// in-flight state survives a host reboot.
export const APPROVALS_DIR = '/var/lib/claude-slack-bridge/approvals';

// approvalIds are generated by us as `approval_<unix-ms>_<base36>`. Reject
// anything else before it touches the filesystem — guards against path
// traversal via crafted Slack button values.
const APPROVAL_ID_RE = /^approval_\d+_[a-z0-9]+$/;

const logger = new Logger('PermissionMCP');

interface PermissionRequest {
  tool_name: string;
  input: any;
  channel?: string;
  thread_ts?: string;
  user?: string;
}

interface PermissionResponse {
  behavior: 'allow' | 'deny';
  updatedInput?: any;
  message?: string;
}

// Decision file shape on disk — adds a signature over (approvalId, behavior).
// Bot signs on write; MCP verifies on read. See HMAC notes near signDecision.
interface SignedDecision extends PermissionResponse {
  signature: string;
}

function getHmacSecret(): string {
  const s = process.env.APPROVAL_HMAC_SECRET;
  if (!s || s.length < 32) {
    throw new Error(
      'APPROVAL_HMAC_SECRET is required (>=32 chars). Generate with `openssl rand -hex 32` and append to .env.',
    );
  }
  return s;
}

// Defense-in-depth against forged approval files. With the bot and MCP both
// running as root, an in-process attacker (e.g. a prompt-injected Claude tool
// running as the same user) can read this secret either way — so this is NOT a
// hard boundary against that threat. It does close stale-file replay across
// restarts and accidental writes by other software. The real fix for the
// in-process threat is out-of-band confirmation (e.g. TOTP / second device);
// see PUNCHLIST.
function signDecision(approvalId: string, behavior: string): string {
  return crypto
    .createHmac('sha256', getHmacSecret())
    .update(`${approvalId}|${behavior}`)
    .digest('hex');
}

function verifySignature(approvalId: string, behavior: string, signature: string): boolean {
  if (typeof signature !== 'string' || signature.length === 0) return false;
  const expected = signDecision(approvalId, behavior);
  if (expected.length !== signature.length) return false;
  try {
    return crypto.timingSafeEqual(Buffer.from(expected, 'hex'), Buffer.from(signature, 'hex'));
  } catch {
    return false;
  }
}

function assertValidApprovalId(approvalId: string): void {
  if (!APPROVAL_ID_RE.test(approvalId)) {
    throw new Error(`Invalid approvalId shape: ${JSON.stringify(approvalId).slice(0, 64)}`);
  }
}

// Hard-deny patterns. Returns reason string if the request would terminate or
// destroy the bot's own runtime, otherwise null. Checked BEFORE thread
// auto-approval and BEFORE posting an approval card — even an auto-approved
// thread can't push a self-kill through. Bypassable via sufficiently creative
// shell tricks (write-then-run scripts, indirect via env vars, polyglot
// payloads). This raises the bar against casual prompt injection but is NOT
// a true sandbox — the real fix is non-root tool exec (see PUNCHLIST).
// Conservative bias: false-deny is recoverable (SSH in); false-allow takes
// the bot offline. Cap input length to bound regex work.
const MAX_SCREENED_CMD_LEN = 8192;

const DENIED_BASH_PATTERNS: Array<{ re: RegExp; reason: string }> = [
  // --- direct signal/process kill ---
  { re: /\bpkill\b/i, reason: 'pkill (bot may be the target)' },
  { re: /\bkillall\b/i, reason: 'killall (bot may be the target)' },
  { re: /\bkill\s+(-\w+\s+)?-?\d+/i, reason: 'kill <pid> (bot pid may be the target)' },
  // Single permissive pattern: `systemctl ... <verb> ... claude-slack-bridge` with any flags between.
  { re: /\bsystemctl\b.*\b(stop|disable|mask|kill|restart|reload)\b.*\bclaude-slack-bridge\b/i, reason: 'systemctl <stop|disable|mask|kill|restart|reload> claude-slack-bridge' },
  { re: /\b(shutdown|halt|poweroff|reboot)\b/i, reason: 'shutdown/halt/poweroff/reboot' },
  { re: /\binit\s+0\b/i, reason: 'init 0' },
  // --- destroy state / install ---
  { re: /\brm\s+(-[rRf]+\s+)*[^\s]*\/var\/lib\/claude-slack-bridge/i, reason: 'rm /var/lib/claude-slack-bridge (state dir)' },
  { re: /\brm\s+(-[rRf]+\s+)*[^\s]*\/opt\/claude-slack-bridge/i, reason: 'rm /opt/claude-slack-bridge (install dir)' },
  { re: /\brm\s+(-[rRf]+\s+)*\/(\s|$)/, reason: 'rm /' },
  // --- common indirection patterns. Not exhaustive (write-script-then-run
  //     and polyglot payloads still slip past); escalates the cost of casual
  //     bypass attempts to "Claude has to be deliberately sneaky." ---
  { re: /\beval\b/i, reason: 'eval (indirect execution)' },
  { re: /\bbase64\s+(-d|-D|--decode|-i)\b/i, reason: 'base64 -d (decode-then-execute pattern)' },
  // The `\b-[ce]\b` form does NOT match in practice because `-` is a non-word
  // char and `\b` requires a word/non-word transition; `-c ` has only non-word
  // chars on both sides of the `-`. Use whitespace-or-quote anchors instead.
  { re: /\b(python3?|node|perl|ruby)\b[^\n]*\s-[ce](?=[\s'"])[^\n]*\b(kill|exec|os\.kill|process\.kill|spawn|system)\b/i, reason: 'interpreter -c/-e invoking kill/exec/spawn' },
];

// Tool-call paths under any of these prefixes are off-limits for Write/Edit
// because overwriting them would either kill the bot, corrupt approval
// state, or forge approvals.
const DENIED_FILE_WRITE_PREFIXES: ReadonlyArray<string> = [
  '/etc/systemd/system/claude-slack-bridge.service',
  '/opt/claude-slack-bridge/',
  '/var/lib/claude-slack-bridge/',
];

// Tools excluded from thread auto-approval. Even when the user has opted into
// "Always approve for thread," these keep showing approval cards because the
// blast radius of a single auto-execution is too high for a bulk opt-in.
const THREAD_AUTO_APPROVAL_EXCLUDES = new Set([
  'Write', 'Edit', 'MultiEdit', 'NotebookEdit', 'Task',
]);

// Tools whose input has a file_path (or notebook_path) that we screen against
// DENIED_FILE_WRITE_PREFIXES.
const PATH_SCREENED_TOOLS = new Set([
  'Write', 'Edit', 'MultiEdit', 'NotebookEdit',
]);

function screenForDangerousCommand(toolName: string, input: any): string | null {
  if (toolName === 'Bash') {
    const cmd: unknown = input?.command;
    if (typeof cmd !== 'string' || !cmd) return null;
    if (cmd.length > MAX_SCREENED_CMD_LEN) {
      return `Bash command exceeds ${MAX_SCREENED_CMD_LEN} chars (refusing to screen, denying for safety)`;
    }
    for (const { re, reason } of DENIED_BASH_PATTERNS) {
      if (re.test(cmd)) return reason;
    }
    return null;
  }

  if (PATH_SCREENED_TOOLS.has(toolName)) {
    const rawPath: unknown = input?.file_path ?? input?.notebook_path;
    if (typeof rawPath !== 'string' || !rawPath) return null;
    // Resolve relative paths so `./.env` from a Claude session whose cwd is
    // the bot's install dir doesn't slip past `startsWith('/opt/...')`.
    // path.resolve uses process.cwd() which is the bot's WorkingDirectory
    // (/opt/claude-slack-bridge per the systemd unit).
    const resolved = path.resolve(rawPath);
    for (const prefix of DENIED_FILE_WRITE_PREFIXES) {
      if (resolved === prefix || resolved.startsWith(prefix)) {
        return `${toolName} into ${prefix}* (would corrupt bot install / state / unit file)`;
      }
    }
    return null;
  }

  return null;
}

class PermissionMCPServer {
  private server: Server;
  private slack: WebClient;

  constructor() {
    this.server = new Server(
      {
        name: "permission-prompt",
        version: "1.0.0",
      },
      {
        capabilities: {
          tools: {},
        },
      }
    );

    this.slack = new WebClient(process.env.SLACK_BOT_TOKEN);
    this.setupHandlers();
  }

  private setupHandlers() {
    this.server.setRequestHandler(ListToolsRequestSchema, async () => {
      return {
        tools: [
          {
            name: "permission_prompt",
            description: "Request user permission for tool execution via Slack button",
            inputSchema: {
              type: "object",
              properties: {
                tool_name: {
                  type: "string",
                  description: "Name of the tool requesting permission",
                },
                input: {
                  type: "object",
                  description: "Input parameters for the tool",
                },
                channel: {
                  type: "string",
                  description: "Slack channel ID",
                },
                thread_ts: {
                  type: "string",
                  description: "Slack thread timestamp",
                },
                user: {
                  type: "string",
                  description: "User ID requesting permission",
                },
              },
              required: ["tool_name", "input"],
            },
          },
        ],
      };
    });

    this.server.setRequestHandler(CallToolRequestSchema, async (request) => {
      if (request.params.name === "permission_prompt") {
        return await this.handlePermissionPrompt(request.params.arguments as unknown as PermissionRequest);
      }
      throw new Error(`Unknown tool: ${request.params.name}`);
    });
  }

  private async handlePermissionPrompt(params: PermissionRequest) {
    const { tool_name, input } = params;

    // Get Slack context from environment (passed by Claude handler)
    const slackContextStr = process.env.SLACK_CONTEXT;
    const slackContext = slackContextStr ? JSON.parse(slackContextStr) : {};
    const { channel, threadTs: thread_ts, user, threadAutoApproved } = slackContext;

    // Hard-deny self-kill / self-destroy commands BEFORE auto-approval kicks in.
    // Even a thread the user opted into auto-approve can't push these through.
    const denyReason = screenForDangerousCommand(tool_name, input);
    if (denyReason) {
      logger.warn('Hard-denied dangerous command', { tool_name, reason: denyReason, user });
      return {
        content: [
          {
            type: 'text' as const,
            text: JSON.stringify({
              behavior: 'deny',
              message: `Auto-denied — command matches a self-kill / self-destroy pattern (${denyReason}). If this was intentional, SSH directly to the host instead.`,
            }),
          },
        ],
      };
    }

    // If thread has auto-approval enabled for this user, approve — but ONLY
    // for tools that aren't write/exec on disk. Write/Edit/NotebookEdit/Task
    // always show a card even in opted-in threads, so a single click in a
    // prompt-injected thread can't auto-execute arbitrary file mutations or
    // sub-agent spawns. Bash auto-approves under the denylist (already
    // screened above for self-kill / -destroy patterns).
    if (threadAutoApproved && !THREAD_AUTO_APPROVAL_EXCLUDES.has(tool_name)) {
      logger.info('Auto-approving tool for thread', { tool_name, user, thread_ts });
      return {
        behavior: 'allow' as const,
        updatedInput: input,
        message: 'Auto-approved for thread'
      };
    }

    // Generate unique approval ID
    const approvalId = `approval_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;

    const requestBody = formatToolForCard(tool_name, input);

    // Create approval message with buttons
    const blocks = [
      {
        type: "section",
        text: {
          type: "mrkdwn",
          text: `🔐 *Permission Request* — Claude wants to use \`${tool_name}\`\n\n${requestBody}`
        }
      },
      {
        type: "actions",
        elements: [
          {
            type: "button",
            text: {
              type: "plain_text",
              text: "✅ Approve"
            },
            style: "primary",
            action_id: "approve_tool",
            value: approvalId
          },
          {
            type: "button",
            text: {
              type: "plain_text",
              text: "❌ Deny"
            },
            style: "danger",
            action_id: "deny_tool",
            value: approvalId
          },
          {
            type: "button",
            text: {
              type: "plain_text",
              text: "🔄 Always approve for thread"
            },
            action_id: "approve_thread_always",
            value: approvalId
          }
        ]
      },
      {
        type: "context",
        elements: [
          {
            type: "mrkdwn",
            text: `Requested by: <@${user}> | Tool: ${tool_name}`
          }
        ]
      }
    ];

    try {
      // Send approval request to Slack
      const result = await this.slack.chat.postMessage({
        channel: channel || user || 'general',
        thread_ts: thread_ts,
        blocks,
        text: `Permission request for ${tool_name}` // Fallback text
      });

      // Record the in-flight request so the bot's startup orphan-sweep can
      // mark this card "expired" if we crash between here and chat.update().
      // Best-effort — log on failure but don't bail (the approval flow itself
      // can still proceed; only the cleanup-after-crash story is degraded).
      if (result.ts && result.channel) {
        try {
          writeRequestRecord(approvalId, {
            channel: result.channel,
            ts: result.ts,
            posted_at: Date.now(),
            tool_name,
          });
        } catch (err: any) {
          logger.warn('Failed to record in-flight request', { approvalId, error: err?.message });
        }
      }

      // Wait for user response
      const response = await this.waitForApproval(approvalId);

      // Claude Code's permission-prompt-tool contract requires `updatedInput`
      // on `allow` (it's the input that will actually be passed to the tool).
      // Pass through the original input verbatim — we don't modify it.
      if (response.behavior === 'allow' && response.updatedInput === undefined) {
        response.updatedInput = input;
      }
      if (response.behavior === 'deny' && !response.message) {
        response.message = 'Denied by user';
      }

      // Update the message to show the result
      if (result.ts) {
        await this.slack.chat.update({
          channel: result.channel!,
          ts: result.ts,
          blocks: [
            {
              type: "section",
              text: {
                type: "mrkdwn",
                text: `${response.behavior === 'allow' ? '✅ *Approved*' : '❌ *Denied*'} — \`${tool_name}\`\n\n${requestBody}`
              }
            },
            {
              type: "context",
              elements: [
                {
                  type: "mrkdwn",
                  text: `${response.behavior === 'allow' ? 'Approved' : 'Denied'} by user | Tool: ${tool_name}`
                }
              ]
            }
          ],
          text: `Permission ${response.behavior === 'allow' ? 'approved' : 'denied'} for ${tool_name}`
        });
      }

      // Card has been finalized — clear the in-flight request record so the
      // orphan sweep doesn't try to "expire" an already-resolved card later.
      try { unlinkRequestRecord(approvalId); } catch { /* best-effort */ }

      return {
        content: [
          {
            type: "text",
            text: JSON.stringify(response)
          }
        ]
      };
    } catch (error) {
      logger.error('Error handling permission prompt:', error);
      
      // Default to deny if there's an error
      const response: PermissionResponse = {
        behavior: 'deny',
        message: 'Error occurred while requesting permission'
      };

      return {
        content: [
          {
            type: "text",
            text: JSON.stringify(response)
          }
        ]
      };
    }
  }

  private async waitForApproval(approvalId: string): Promise<PermissionResponse> {
    assertValidApprovalId(approvalId);
    fs.mkdirSync(APPROVALS_DIR, { recursive: true, mode: 0o700 });
    const filePath = path.join(APPROVALS_DIR, `${approvalId}.json`);

    const TIMEOUT_MS = 5 * 60 * 1000;
    const POLL_MS = 300;
    const startedAt = Date.now();

    while (Date.now() - startedAt < TIMEOUT_MS) {
      try {
        const content = fs.readFileSync(filePath, 'utf-8');
        const decision = JSON.parse(content) as Partial<SignedDecision>;
        // Always consume the file, even if it's invalid — keeps stale forgeries
        // from accumulating and prevents re-reading the same bad file.
        try { fs.unlinkSync(filePath); } catch { /* best-effort cleanup */ }

        if (
          decision.behavior !== 'allow' && decision.behavior !== 'deny'
        ) {
          logger.warn('Decision file has invalid behavior — rejecting', { approvalId });
          return { behavior: 'deny', message: 'Invalid decision file' };
        }
        if (!verifySignature(approvalId, decision.behavior, decision.signature || '')) {
          logger.warn('Decision signature invalid — rejecting forged file', { approvalId });
          return { behavior: 'deny', message: 'Forged approval rejected' };
        }

        logger.info('Approval received via file IPC', { approvalId, behavior: decision.behavior });
        return {
          behavior: decision.behavior,
          updatedInput: decision.updatedInput,
          message: decision.message,
        };
      } catch (err: any) {
        if (err && err.code !== 'ENOENT') {
          logger.warn('Error reading approval file', { approvalId, error: err.message });
        }
      }
      await new Promise((r) => setTimeout(r, POLL_MS));
    }

    logger.warn('Approval timed out', { approvalId });
    return { behavior: 'deny', message: 'Permission request timed out' };
  }

  async run() {
    const transport = new StdioServerTransport();
    await this.server.connect(transport);
    logger.info('Permission MCP server started');
  }
}

// In-flight request record. Written by the MCP subprocess after it posts the
// approval card to Slack; consumed by the main bot's startup orphan-sweep to
// mark abandoned cards "expired" (covers the bot-crashed-mid-approval case).
export interface RequestRecord {
  channel: string;
  ts: string;
  posted_at: number;
  tool_name: string;
}

function requestPathFor(approvalId: string): string {
  assertValidApprovalId(approvalId);
  return path.join(APPROVALS_DIR, `${approvalId}.request.json`);
}

export function writeRequestRecord(approvalId: string, rec: RequestRecord): void {
  fs.mkdirSync(APPROVALS_DIR, { recursive: true, mode: 0o700 });
  const finalPath = requestPathFor(approvalId);
  const tmpPath = finalPath + '.tmp';
  fs.writeFileSync(tmpPath, JSON.stringify({ approvalId, ...rec }), { mode: 0o600 });
  fs.renameSync(tmpPath, finalPath);
}

export function unlinkRequestRecord(approvalId: string): void {
  try { fs.unlinkSync(requestPathFor(approvalId)); } catch { /* best-effort */ }
}

export function listOrphanRequests(olderThanMs: number): Array<RequestRecord & { approvalId: string; filePath: string }> {
  const out: Array<RequestRecord & { approvalId: string; filePath: string }> = [];
  let entries: string[];
  try {
    entries = fs.readdirSync(APPROVALS_DIR);
  } catch (err: any) {
    if (err?.code === 'ENOENT') return out;
    throw err;
  }
  const now = Date.now();
  for (const name of entries) {
    if (!name.endsWith('.request.json')) continue;
    const filePath = path.join(APPROVALS_DIR, name);
    try {
      const raw = fs.readFileSync(filePath, 'utf-8');
      const meta = JSON.parse(raw);
      const age = now - (typeof meta.posted_at === 'number' ? meta.posted_at : 0);
      if (age < olderThanMs) continue;
      out.push({ ...meta, filePath });
    } catch {
      // Unparseable / partially-written — let caller decide whether to nuke.
      out.push({
        approvalId: name.replace(/\.request\.json$/, ''),
        channel: '', ts: '', posted_at: 0, tool_name: 'unknown',
        filePath,
      });
    }
  }
  return out;
}

// Cross-process IPC: write the approval decision to a file the MCP subprocess
// polls. Atomic-ish via write-then-rename so the subprocess never reads a
// partially written file. Called from the main bot process on button click.
// Signs the decision with APPROVAL_HMAC_SECRET so the MCP can detect forgeries.
export function writeApprovalDecision(
  approvalId: string,
  decision: { behavior: 'allow' | 'deny'; updatedInput?: any; message?: string },
): void {
  assertValidApprovalId(approvalId);
  fs.mkdirSync(APPROVALS_DIR, { recursive: true, mode: 0o700 });
  const signed: SignedDecision = {
    ...decision,
    signature: signDecision(approvalId, decision.behavior),
  };
  const finalPath = path.join(APPROVALS_DIR, `${approvalId}.json`);
  const tmpPath = finalPath + '.tmp';
  fs.writeFileSync(tmpPath, JSON.stringify(signed), { mode: 0o600 });
  fs.renameSync(tmpPath, finalPath);
}

// Pretty-print a tool invocation for the Slack approval card. Mirrors
// formatToolUse in slack-handler.ts but without the post-approval prefix.
function truncate(s: string | undefined, n: number): string {
  if (!s) return '';
  return s.length <= n ? s : s.substring(0, n) + '\n…[truncated]';
}

function formatToolForCard(toolName: string, input: any): string {
  switch (toolName) {
    case 'Edit':
    case 'MultiEdit': {
      const filePath = input.file_path;
      const edits = toolName === 'MultiEdit'
        ? (input.edits || [])
        : [{ old_string: input.old_string, new_string: input.new_string }];
      let body = `📝 *${filePath}*\n`;
      for (const edit of edits) {
        const oldLines = truncate(edit.old_string, 600).split('\n');
        const newLines = truncate(edit.new_string, 600).split('\n');
        body += '```diff\n';
        for (const line of oldLines) body += `- ${line}\n`;
        for (const line of newLines) body += `+ ${line}\n`;
        body += '```\n';
      }
      return body;
    }
    case 'Write': {
      const filePath = input.file_path;
      const preview = truncate(input.content, 800);
      return `📄 *Creating* \`${filePath}\`\n\`\`\`\n${preview}\n\`\`\``;
    }
    case 'Bash': {
      const cmd = truncate(input.command, 1000);
      const desc = input.description ? `_${input.description}_\n` : '';
      return `💻 *Running command*\n${desc}\`\`\`bash\n${cmd}\n\`\`\``;
    }
    case 'NotebookEdit': {
      const p = input.notebook_path;
      return `📓 *Editing notebook* \`${p}\`\n\`\`\`\n${truncate(input.new_source, 500)}\n\`\`\``;
    }
    case 'Task': {
      const subagent = input.subagent_type || 'general-purpose';
      const desc = input.description || '';
      return `🤖 *Spawning ${subagent} subagent*\n_${desc}_\n\`\`\`\n${truncate(input.prompt, 500)}\n\`\`\``;
    }
    default:
      return `*Parameters:*\n\`\`\`json\n${truncate(JSON.stringify(input, null, 2), 1000)}\n\`\`\``;
  }
}

// Export singleton instance for use by Slack handler
export const permissionServer = new PermissionMCPServer();

// Run if this file is executed directly (not imported)
if (process.argv[1]?.includes('permission-mcp-server')) {
  permissionServer.run().catch((error) => {
    logger.error('Permission MCP server error:', error);
    process.exit(1);
  });
}